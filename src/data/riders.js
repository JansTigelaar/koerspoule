// Helper functies
export const getRiderById = (id) => {
  return RIDERS.find(r => r.id === id);
};

export const getRiderByNumber = (number) => {
  return RIDERS.find(r => r.number === number);
};

export const searchRiders = (query) => {
  const q = query.toLowerCase();
  return RIDERS.filter(r => 
    r.name.toLowerCase().includes(q) || 
    r.number.toString().includes(q)
  );
};

export const getJokerRiders = () => {
  return RIDERS.filter(r => r.isJoker);
};

export const getYoungRiders = () => {
  return RIDERS.filter(r => r.isYoungRider);
};
